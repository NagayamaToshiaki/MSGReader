    #region GetConfidence
    public float GetConfidence()
    {
        // This is just one way to calculate confidence. It works well for me.
        if (_totalRel > MINIMUM_DATA_THRESHOLD)
            return (float)(_totalRel - _relSample[0]) / _totalRel;
        return DONT_KNOW;
    }
